--emp테이블에서 입사 20주년이 되는 사원들의 데이터 출력
--empno,ename,hiredate

SELECT empno,
       ename,
       hiredate,
       ADD_MONTHS(hiredate,12*10*2) AS WORK20YEAR
FROM emp
;


