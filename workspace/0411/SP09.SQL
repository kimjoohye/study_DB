--생성
CREATE OR REPLACE PROCEDURE PRO_NOPARAM
IS
    V_EMPNO NUMBER(4) := 7788;
    V_ENAME VARCHAR2(10);
BEGIN
    V_ENAME := 'SCOTT';
    DBMS_OUTPUT.PUT_LINE('V_EMPNO: ' || V_EMPNO);
    DBMS_OUTPUT.PUT_LINE('V_ENAME: ' || V_ENAME);
END;
/

--실행
--EXECUTE 프로시저 이름
SET SERVEROUTPUT ON;
EXECUTE PRO_NOPARAM;

